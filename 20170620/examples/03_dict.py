### keys
a = {'name': 'pey', 'phone': '0119993323', 'birth': '1118'}
print(a.keys())

### values
print(a.values())

### items
print(a.items())
[('birth', '1118'), ('name', 'pey'), ('phone', '011993323')]

### clear
a.clear()
print(a)

### get
a = {'name':'pey', 'phone':'0119993323', 'birth': '1118'}
print(a.get('name'))

### has_key
a = {'name':'pey', 'phone':'0119993323', 'birth': '1118'}
print(a.has_key('name'))
