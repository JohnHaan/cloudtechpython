### append
a = [1, 2, 3]
a.append(4)
print(a)

### sort
a = [1, 4, 3, 2]
a.sort()
print(a)

### reverse
a = ['a', 'c', 'b']
a.reverse()
print(a)

### insert
a = [1,2,3]
a.insert(0, 4)
print(a)

### remove
a = [1,2,3,1,2,3]
a.remove(3)
print(a)

### pop
a = [1,2,3]
a.pop()
print(a)

a = [1,2,3]
a.pop(1)
print(a)

### count
a = [1,2,3,1]
print(a.count(1))

### extend
a = [1,2,3]
a.extend([4,5])
print(a)
